bool ok(int n)
{
    for (int i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
int32_t main()
{
    MTK;
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        vector<int> v;

        for (int i = 2;; i++)
        {
            if (ok(i))
                v.push_back(i);
            if (v.size() == n)
                break;
        }
        for (auto x : v)
            cout << x << ' ';
        cout << '\n';
    }
    return 0;
}